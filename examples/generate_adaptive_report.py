def calculate_position(regime, confidence, trend_strength):
    if regime == 'bull':
        return min(0.3 + confidence * 0.7, 1.0)  # 30%-100%
    elif regime == 'bear':
        return max(0.5 - confidence * 0.3, 0.1)  # 50%-10%
    else:
        return 0.5 + trend_strength * 0.2        # 30%-70%
